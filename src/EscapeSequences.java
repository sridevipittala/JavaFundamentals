public class EscapeSequences {
    public static  void main(String[] args) {
        /*
        Write a program that demonstrates ALL of the following escape sequences in meaningful
        sentences:  \n (newline), \t (tab), \\ (backslash), \" (double quote), \' (single quote).
        Print a formatted receipt-style output using tabs to align: Item name (left), Price (right-aligned using tabs).
        Hint: You cannot use printf for this question — only println and escape characters.
        Expected Output (sample):
        ---- RECEIPT ----
        Item\t\t\tPrice
        Coffee\t\t\tRs. 50
        Sandwich\t\tRs. 120
         */
        System.out.println("Item\t\t\tPrice");
        System.out.println("Coffee\t\t\tRs. 50");
        System.out.println("Sandwich\t\tRs. 120");

    }
}
